dso::vmf3_details::Vmf3FullCoeffs
dso::vmf3_details::vmf3FullCoeffSet(double lon, double lat) noexcept {
  const double polDist = M_PI / 2 - lat;
  const double x = std::sin(polDist) * std::cos(lon);
  const double y = std::sin(polDist) * std::sin(lon);
  const double z = std::cos(polDist);

  /* Legendre polynomials */
  int nmax = 12;
  LwMat12 V, W;
  double *Vcol0 = V.col(0);
  double *Wcol0 = W.col(0);
  *Vcol0 = 1e0;
  Vcol0[1] = z * Vcol0[0];
  *Wcol0 = 0e0;
  Wcol0[1] = 0e0;

  /* column 0 */
  for (int n = 1; n <= nmax; n++) {
    int p = n + 1;
    Vcol0[n + 1] = ((2 * p - 1) * z * Vcol0[n] - (p - 1) * Vcol0[n - 1]) / (p);
    Wcol0[n + 1] = 0e0;
  }

  for (int m = 1; m <= nmax; m++) {
    /* pointer to start of column */
    double *__restrict__ Vc = V.col(m);
    double *__restrict__ Wc = W.col(m);
    /* diagonal */
    Vc[0] = (2 * m - 1) * (x * *V.col(m - 1) - y * *W.col(m - 1));
    Wc[0] = (2 * m - 1) * (x * *W.col(m - 1) + y * *V.col(m - 1));
    /* below diagonal */
    if (m < nmax) {
      Vc[1] = (2 * m + 1) * z * Vc[0];
      Wc[1] = (2 * m + 1) * z * Wc[0];
    }
    /* column m, rows [m+2,:] */
    int k = 2;
    for (int n = m + 2; n <= nmax; n++) {
      Vc[k] = ((2 * n - 1) * z * Vc[k - 1] - (n + m - 1) * Vc[k - 2]) / (n - m);
      Wc[k] = ((2 * n - 1) * z * Wc[k - 1] - (n + m - 1) * Wc[k - 2]) / (n - m);
      // printf("V(%d,%d) %d * %.3f * %.3f - %d * %.3f / %d \n", n,m,2 * n -
      // 1,z,Vc[k-1],n+m-1,Vc[k-2],n-m);
      ++k;
      assert(k < LwMat12Size());
    }
  }

  dso::vmf3_details::Vmf3FullCoeffs vc;
  /* determine the coefficients bh, bw, ch and cw */
  int i = 0;
  for (int n = 0; n <= nmax; n++) {
    for (int m = 0; m <= n; m++) {
      vc.bh_A0 += (anm_bh[0][i] * V(n, m) + bnm_bh[0][i] * W(n, m));
      vc.bh_A1 += (anm_bh[1][i] * V(n, m) + bnm_bh[1][i] * W(n, m));
      vc.bh_B1 += (anm_bh[2][i] * V(n, m) + bnm_bh[2][i] * W(n, m));
      vc.bh_A2 += (anm_bh[3][i] * V(n, m) + bnm_bh[3][i] * W(n, m));
      vc.bh_B2 += (anm_bh[4][i] * V(n, m) + bnm_bh[4][i] * W(n, m));

      vc.bw_A0 += (anm_bw[0][i] * V(n, m) + bnm_bw[0][i] * W(n, m));
      vc.bw_A1 += (anm_bw[1][i] * V(n, m) + bnm_bw[1][i] * W(n, m));
      vc.bw_B1 += (anm_bw[2][i] * V(n, m) + bnm_bw[2][i] * W(n, m));
      vc.bw_A2 += (anm_bw[3][i] * V(n, m) + bnm_bw[3][i] * W(n, m));
      vc.bw_B2 += (anm_bw[4][i] * V(n, m) + bnm_bw[4][i] * W(n, m));

      vc.ch_A0 += (anm_ch[0][i] * V(n, m) + bnm_ch[0][i] * W(n, m));
      vc.ch_A1 += (anm_ch[1][i] * V(n, m) + bnm_ch[1][i] * W(n, m));
      vc.ch_B1 += (anm_ch[2][i] * V(n, m) + bnm_ch[2][i] * W(n, m));
      vc.ch_A2 += (anm_ch[3][i] * V(n, m) + bnm_ch[3][i] * W(n, m));
      vc.ch_B2 += (anm_ch[4][i] * V(n, m) + bnm_ch[4][i] * W(n, m));

      vc.cw_A0 += (anm_cw[0][i] * V(n, m) + bnm_cw[0][i] * W(n, m));
      vc.cw_A1 += (anm_cw[1][i] * V(n, m) + bnm_cw[1][i] * W(n, m));
      vc.cw_B1 += (anm_cw[2][i] * V(n, m) + bnm_cw[2][i] * W(n, m));
      vc.cw_A2 += (anm_cw[3][i] * V(n, m) + bnm_cw[3][i] * W(n, m));
      vc.cw_B2 += (anm_cw[4][i] * V(n, m) + bnm_cw[4][i] * W(n, m));

      ++i;
      assert(i <= 91);
    }
  }

  return vc;
}

dso::vmf3_details::Vmf3EmpiricalCoeffs
dso::vmf3_details::Vmf3FullCoeffs::computeCoeffs(
    const dso::MjdEpoch &t) const noexcept {
  /* fractional day of year */
  const dso::ydoy_date yd(t.to_ydoy());
  const double doy = yd.dy().as_underlying_type() + t.fractional_days();

  constexpr const double pi = M_PI;
  dso::vmf3_details::Vmf3EmpiricalCoeffs vc;

  /* trigs */
  const double sy2 = std::sin(doy / 365.25 * 2 * pi);
  const double cy2 = std::cos(doy / 365.25 * 2 * pi);
  const double sy4 = std::sin(doy / 365.25 * 4 * pi);
  const double cy4 = std::cos(doy / 365.25 * 4 * pi);

  /* add seasonal amplitudes for the specified doy to the mean values */
  vc.bh = bh_A0 + bh_A1 * cy2 + bh_B1 * sy2 + bh_A2 * cy4 + bh_B2 * sy4;
  vc.bw = bw_A0 + bw_A1 * cy2 + bw_B1 * sy2 + bw_A2 * cy4 + bw_B2 * sy4;
  vc.ch = ch_A0 + ch_A1 * cy2 + ch_B1 * sy2 + ch_A2 * cy4 + ch_B2 * sy4;
  vc.cw = cw_A0 + cw_A1 * cy2 + cw_B1 * sy2 + cw_A2 * cy4 + cw_B2 * sy4;

  return vc;
}
